package Pattern_Question_Revision;

public class Solid_Rectangle {
    
    public static void main(String[] args)
    {
    //SOLID RECTANGLE
    int n = 4; //no. of rows
    int m = 5; //no. of columns
    //OUTER LOOP
    for (int i=1;i<=n;i++){
        for (int j=1;j<=m;j++){
            System.out.print("*");
        }
        System.out.println();
    }
    
    }
 
    
}
