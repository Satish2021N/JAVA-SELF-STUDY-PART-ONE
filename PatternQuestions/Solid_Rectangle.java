package PatternQuestions;

public class Solid_Rectangle {
    //SOLID RECTANGLE
    public static void main (String[] args)
    {
        //OUTER LOOP
        for(int i=1;i<=4;i++){
            //INNER LOOP
            for(int j=1;j<=5;j++){
                System.out.print("*");
            }
            System.out.println();
        }
    }   
}
